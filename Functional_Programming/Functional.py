# Lambda Functions
# Anonymous functions(nameless functions)

f=lambda no1,no2: no1+no2
print(f(10,3))

sub=lambda no1,no2: no1-no2
print(sub(10,3))


mul=lambda no1,no2: no1*no2
print(mul(10,3))

cub=lambda  no:no**3
print(cub(2))

odd=lambda num:num%2!=0
print(odd(4))

up=lambda name:name.upper()
print(up("hello"))